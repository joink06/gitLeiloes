    private void listarVendas(){
        try{
            ProdutosDAO dao= new ProdutosDAO();
            
            DefaultTableModel modelo = (DefaultTableModel) tblVendidos.getModel();
            modelo.setNumRows(0);
            
            ArrayList<ProdutosDTO> lista = dao.listarProdutosVendidos();
            
            for(ProdutosDTO p : lista){
                Object[] obj = new Object[]{
                    p.getId(),
                    p.getNome(),
                    p.getValor(),
                    p.getStatus()
                };
                
                modelo.addRow(obj);
            }
            
        }catch(Exception ex){
            
        }
    }

